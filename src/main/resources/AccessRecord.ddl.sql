CREATE TABLE IF NOT EXISTS `ACCESS_RECORD` (
  `SESSION_ID` char(36) CHARACTER SET latin1 COLLATE latin1_bin NOT NULL,
  `RETURN_OBJECT_ID` varchar(22) CHARACTER SET latin1 COLLATE latin1_bin DEFAULT NULL,
  `ELAPSE_MS` bigint NOT NULL,
  `TIMESTAMP` bigint NOT NULL,
  `VIA` varchar(100) CHARACTER SET latin1 COLLATE latin1_bin DEFAULT NULL,
  `HOST` varchar(100) CHARACTER SET latin1 COLLATE latin1_bin DEFAULT NULL,
  `THREAD_ID` bigint NOT NULL,
  `USER_AGENT` varchar(150) CHARACTER SET latin1 COLLATE latin1_bin DEFAULT NULL,
  `QUERY_STRING` varchar(3000) CHARACTER SET latin1 COLLATE latin1_bin DEFAULT NULL,
  `X_FORWARDED_FOR` varchar(100) CHARACTER SET latin1 COLLATE latin1_bin DEFAULT NULL,
  `REQUEST_URL` varchar(150) CHARACTER SET latin1 COLLATE latin1_bin NOT NULL,
  `USER_ID` bigint DEFAULT NULL,
  `ORIGIN` varchar(100) CHARACTER SET latin1 COLLATE latin1_bin DEFAULT NULL,
  `DATE` date NOT NULL,
  `METHOD` ENUM('GET','PUT','POST', 'DELETE') NOT NULL,
  `VM_ID` varchar(50) CHARACTER SET latin1 COLLATE latin1_bin NOT NULL,
  `INSTANCE` bigint NOT NULL,
  `STACK` char(4) NOT NULL,
  `SUCCESS` bit(1) NOT NULL,
  `RESPONSE_STATUS` int NOT NULL,
  `OAUTH_CLIENT_ID` varchar(22) CHARACTER SET latin1 COLLATE latin1_bin DEFAULT NULL,
  PRIMARY KEY (`SESSION_ID`, `TIMESTAMP`),
  INDEX (RETURN_OBJECT_ID),
  INDEX (ELAPSE_MS),
  INDEX (TIMESTAMP),
  INDEX (USER_ID),
  INDEX (METHOD),
  INDEX (RESPONSE_STATUS)
)
