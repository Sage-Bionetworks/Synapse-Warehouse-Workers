CREATE TABLE IF NOT EXISTS `NODE_SNAPSHOT` (
  `TIMESTAMP` bigint NOT NULL,
  `ID` bigint(20) NOT NULL,
  `BENEFACTOR_ID` bigint(20) DEFAULT NULL,
  `PROJECT_ID` bigint(20) DEFAULT NULL,
  `PARENT_ID` bigint(20) DEFAULT NULL,
  `NODE_TYPE` ENUM('project', 'folder', 'file', 'table', 'link') NOT NULL,
  `CREATED_ON` bigint(20) NOT NULL,
  `CREATED_BY` bigint(20) NOT NULL,
  `MODIFIED_ON` bigint(20) NOT NULL,
  `MODIFIED_BY` bigint(20) NOT NULL,
  `VERSION_NUMBER` bigint DEFAULT NULL,
  `FILE_HANDLE_ID` bigint DEFAULT NULL,
  `NAME` varchar(256) CHARACTER SET latin1 COLLATE latin1_bin DEFAULT NULL,
  PRIMARY KEY (`ID`, `TIMESTAMP`),
  INDEX (BENEFACTOR_ID),
  INDEX (PROJECT_ID),
  INDEX (PARENT_ID),
  INDEX (CREATED_BY),
  INDEX (MODIFIED_BY)
)
